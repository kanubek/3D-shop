#!/bin/bash
# Скрипт для автоматического сохранения изменений в GitHub

cd "/home/rokket/Документы/3d-shop/3D-shop"

# Проверяем есть ли изменения
if [[ -n $(git status --porcelain) ]]; then
    echo "Обнаружены изменения, сохраняем..."
    
    # Добавляем все файлы
    git add .
    
    # Коммитим с временной меткой
    git commit -m "Автоматическое сохранение: $(date '+%d.%m.%Y %H:%M:%S')"
    
    # Пушим на GitHub
    git push
    
    echo "Изменения успешно сохранены в GitHub!"
else
    echo "Нет изменений для сохранения."
fi